
# Control Structures: If-Else, Loops

# If-Else
num <- 10
if (num > 0) {
  print("Positive Number")
} else {
  print("Non-Positive Number")
}

# For Loop
for (i in 1:5) {
  print(paste("Iteration:", i))
}

# While Loop
count <- 1
while (count <= 5) {
  print(paste("Count is", count))
  count <- count + 1
}
